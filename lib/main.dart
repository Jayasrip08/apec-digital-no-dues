/// This widget listens to the Auth State (Logged In vs Logged Out)
class AuthWrapper extends StatelessWidget {
  const AuthWrapper({super.key});

  @override
  Widget build(BuildContext context) {
    return StreamBuilder<User?>(
      stream: FirebaseAuth.instance.authStateChanges(),
      builder: (context, snapshot) {
        // 1. If waiting for connection
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const Scaffold(body: Center(child: CircularProgressIndicator()));
        }

        // 2. If User is Logged In -> Determine Role & Route
        if (snapshot.hasData && snapshot.data != null) {
          return const RoleResolver();
        }

        // 3. If User is Logged Out -> Show Login Screen
        return const LoginScreen();
      },
    );
  }
}

/// Fetches the User's Role from Firestore to decide which Dashboard to show
class RoleResolver extends StatelessWidget {
  const RoleResolver({super.key});

  @override
  Widget build(BuildContext context) {
    final User user = FirebaseAuth.instance.currentUser!;

    return FutureBuilder<DocumentSnapshot>(
      future: FirebaseFirestore.instance.collection('users').doc(user.uid).get(),
      builder: (context, snapshot) {
        // Loading State
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const Scaffold(
            body: Center(
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  CircularProgressIndicator(),
                  SizedBox(height: 20),
                  Text("Fetching Profile..."),
                ],
              ),
            ),
          );
        }

        // Error or No Data
        if (snapshot.hasError || !snapshot.hasData || !snapshot.data!.exists) {
          return Scaffold(
            body: Center(
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  const Icon(Icons.error_outline, size: 50, color: Colors.red),
                  const SizedBox(height: 10),
                  const Text("Error fetching user profile."),
                  TextButton(
                    onPressed: () => FirebaseAuth.instance.signOut(), 
                    child: const Text("Logout & Try Again")
                  ),
                ],
              ),
            ),
          );
        }

        // Data Loaded -> Check Role
        final data = snapshot.data!.data() as Map<String, dynamic>;
        final String role = data['role'] ?? 'student';

        if (role == 'admin') {
          return const AdminDashboard();
        } else if (role == 'staff') {
          return const StaffDashboard();
        } else {
          return const StudentDashboard();
        }
      },
    );
  }
}